package com.myapp.struts.election_manager;

import com.myapp.struts.hbm.Institute;
import com.myapp.struts.hbm.InstituteDAO;
import java.util.List;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;
import org.apache.struts.action.ActionForm;
import org.apache.struts.action.ActionForward;
import org.apache.struts.action.ActionMapping;

/**
 *
 * @author Edrp-04
 */
public class AddVoterAction extends org.apache.struts.action.Action {
    
    /* forward name="success" path="" */
    private static final String SUCCESS = "success";
    
    
    @Override
    public ActionForward execute(ActionMapping mapping, ActionForm form,
            HttpServletRequest request, HttpServletResponse response)
            throws Exception {
        HttpSession session = request.getSession();
        String Button="Add";
        InstituteDAO dao = new InstituteDAO();
        List<Institute> lst = dao.getInstituteNameByStatus("OK");
        session.setAttribute("instituteList", lst);
        request.setAttribute("button", Button);
        session.removeAttribute("image");
        return mapping.findForward(SUCCESS);
    }
}
